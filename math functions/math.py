import math

# 1. math.sqrt() - Square Root
result = math.sqrt(16)
print(result)  # Output: 4.0

# 2. math.pow() - Power Function
result = math.pow(2, 3)
print(result)  # Output: 8.0

# 3. math.factorial() - Factorial
result = math.factorial(5)
print(result)  # Output: 120

# 4. math.fabs() - Absolute Value
result = math.fabs(-10)
print(result)  # Output: 10.0

# 5. math.radians() - Convert Degrees to Radians
result = math.radians(180)
print(result)  # Output: 3.141592653589793

# 6. math.degrees() - Convert Radians to Degrees
result = math.degrees(math.pi)
print(result)  # Output: 180.0

# 7. math.ceil() - Round Up
result = math.ceil(4.2)
print(result)  # Output: 5

# 8. math.floor() - Round Down
result = math.floor(4.8)
print(result)  # Output: 4

# 9. math.log() - Logarithm
result = math.log(1000, 10)
print(result)  # Output: 3.0

# 10. math.sin(), math.cos(), math.tan() - Trigonometric Functions

# Sine of 90 degrees (converted to radians)
result_sin = math.sin(math.radians(90))
print(result_sin)  # Output: 1.0

# Cosine of 0 degrees (converted to radians)
result_cos = math.cos(math.radians(0))
print(result_cos)  # Output: 1.0

# Tangent of 45 degrees (converted to radians)
result_tan = math.tan(math.radians(45))
print(result_tan)  # Output: 1.0

